// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode* root) {
        queue<TreeNode *> q;
        vector<vector<int>> res;

        if (root) {
            q.push(root);
        }
        while (!q.empty()) {
            vector<int> level;
            int count = q.size();
            while(count--) {
                auto front = q.front();
                q.pop();

                level.push_back(front->val);

                if (front->left) {
                    q.push(front->left);
                }
                if (front->right) {
                    q.push(front->right);
                }
            }
            res.push_back(level);
        }
        return res;
    }
};
// @lc code=end

